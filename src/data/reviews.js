// Function to get reviews for a specific product
export const getProductReviews = (productId) => {
  return reviews.filter(review => review.productId === productId);
};

// Function to get average rating for a product
export const getProductAverageRating = (productId) => {
  const productReviews = getProductReviews(productId);
  if (productReviews.length === 0) return 0;

  const totalRating = productReviews.reduce((sum, review) => sum + review.rating, 0);
  return totalRating / productReviews.length;
};

// Function to add a new review (in a real app, this would be an API call)
export const addReview = (review) => {
  const newReview = {
    ...review,
    id: reviews.length + 1,
    date: new Date().toISOString().split('T')[0],
    helpfulCount: 0,
    verified: true
  };

  reviews.push(newReview);
  return newReview;
};

// Function to mark a review as helpful
export const markReviewAsHelpful = (reviewId) => {
  const review = reviews.find(r => r.id === reviewId);
  if (review) {
    review.helpfulCount += 1;
    return true;
  }
  return false;
};
